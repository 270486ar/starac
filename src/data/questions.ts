export const getQuestionsByFilters = (
  board?: Board,
  className?: ClassName,
  subject?: Subject,
  chapter?: string
): Question[] => {
  return questions.filter(
    (q) =>
      (!board || q.board === board) &&
      (!className || q.className === className) &&
      (!subject || q.subject === subject) &&
      (!chapter || q.chapter === chapter)
  );
};

export const getChapters = (board: Board, className: ClassName, subject: Subject): string[] => {
  const filtered = questions.filter(
    (q) => q.board === board && q.className === className && q.subject === subject
  );
  return [...new Set(filtered.map((q) => q.chapter))];
};

export const getSubjects = (board: Board, className: ClassName): Subject[] => {
  const filtered = questions.filter(
    (q) => q.board === board && q.className === className
  );
  return [...new Set(filtered.map((q) => q.subject))];
};

export const getQuestionOfTheDay = (): Question => {
  const dayIndex = new Date().getDate() % questions.length;
  return questions[dayIndex];
};
