export const getTestById = (id: string): TestInfo | undefined => {
  return availableTests.find((t) => t.id === id);
};

export const getTestsByFilters = (
  board?: string,
  className?: string,
  subject?: string
): TestInfo[] => {
  return availableTests.filter(
    (t) =>
      (!board || t.board === board) &&
      (!className || t.className === className) &&
      (!subject || t.subject === subject)
  );
};
